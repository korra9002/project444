package userVO;

public class FlagVO {
	private String dealFLag, allFlag;

	public FlagVO(String dealFLag, String allFlag) {
		super();
		this.dealFLag = dealFLag;
		this.allFlag = allFlag;
	}

	public String getDealFLag() {
		return dealFLag;
	}

	public String getAllFlag() {
		return allFlag;
	}

}
